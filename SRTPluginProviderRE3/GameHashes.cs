        public static GameVersion DetectVersion(string filePath)
        {
            byte[] checksum;
            using (SHA256 hashFunc = SHA256.Create())
            using (FileStream fs = new FileStream(filePath, FileMode.Open, FileAccess.Read, FileShare.ReadWrite | FileShare.Delete))
                checksum = hashFunc.ComputeHash(fs);

            if (checksum.SequenceEqual(re3WW_20200930_1))
                return GameVersion.RE3_WW_20200930_1;
            else if (checksum.SequenceEqual(re3WW_20200806_1))
                return GameVersion.RE3_WW_20200806_1;
            else if (checksum.SequenceEqual(bio3CEROZ_20200806_1))
                return GameVersion.BIO3_CEROZ_20200806_1;
            else if (checksum.SequenceEqual(re3WW_20200603_1))
                return GameVersion.RE3_WW_20200603_1;
            else if (checksum.SequenceEqual(bio3CEROZ_20200603_1))
                return GameVersion.BIO3_CEROZ_20200603_1;
            else
                return GameVersion.Unknown;
        }
